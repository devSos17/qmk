static void print_slave(void) {
    oled_set_cursor(1, 1);
    render_logo();

    render_luna(0, 10); /* KEYBOARD PET RENDER  */

    /* wpm counter */
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(2, 14);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;

    oled_write(wpm_str, false);

    oled_set_cursor(2, 15);
    oled_write("wpm", false);
}

static void print_master(void) {
    // Ma name
    oled_set_cursor(1, 1);
    oled_write("dev", false);
    oled_set_cursor(2, 2);
    oled_write("Sos", false);

    oled_set_cursor(0, 6);
    oled_write("LAYER", false); /* Print current layer */
    oled_set_cursor(0, 8);
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_P(PSTR("Base\n"), false);
            break;
        case _SYMBOLS:
            oled_write_P(PSTR("Signs\n"), false);
            break;
        case _NUMPAD:
            oled_write_P(PSTR("Nums\n"), false);
            break;
        case _GAMEPAD:
            oled_write_P(PSTR("Game\n"), false);
            break;
        default:
            oled_write_ln_P(PSTR("Undef"), false);
    }

    /* caps lock */
    oled_set_cursor(0, 13);
    oled_write("CPSLK", led_usb_state.caps_lock);

    oled_set_cursor(0, 14);
    oled_write("NUMLK", led_usb_state.num_lock);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) { return OLED_ROTATION_270; }

bool oled_task_user(void) {
    /* KEYBOARD PET VARIABLES START */

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    /* KEYBOARD PET VARIABLES END */

    if (is_keyboard_master()) {
        print_master();
    } else {
        print_slave();
    }
    return false;
}

